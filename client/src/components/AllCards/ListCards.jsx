import { useState , useEffect } from "react";
import food from "./food.webp";
import { useNavigate } from "react-router-dom";
import axios from "axios"
const ListCards = () => {
  const navigate = useNavigate();
  const [search, setSearch] = useState("");
  const [data, setData] = useState([]);

  useEffect(() => {
    const fetchData = async () => {
      try {
        
        const response = await axios.get("http://localhost:3030/hotels");
        setData(response.data);
      } catch (err) {
        console.log("error", err);
      }
    };
    fetchData();
  }, []);

  const navToDetails = (restaurantId) => {
    navigate(`/restaurant_detail/${restaurantId}`);
  };

  // Sample data for restaurant cards


  return (
    <div className="min-h-screen flex flex-col items-center justify-center">
      <input
        className="w-1/2 mb-8 mt-8 h-8 px-2 py-3 rounded-md text-white text-2xl bg-black border-black"
        placeholder="Search"
        value={search}
        onChange={(e) => setSearch(e.target.value)}
      />
      <div className="flex flex-wrap justify-center gap-6">
        {data.map((restaurant) => (
          <div
            key={restaurant.HotelId}
            className="w-70 p-2 bg-white rounded-xl transform transition-all hover:-translate-y-2 duration-300 shadow-lg hover:shadow-xl cursor-pointer"
            onClick={() => navToDetails(restaurant.HotelId)}
          >
            <img src={restaurant.ImageUrl} alt="Restaurant" className="h-50 w-80 rounded-xl object-cover" />
            <div className="p-2">
              <div className="flex flex-row justify-between">
                <h2 className="font-bold text-lg mb-2">{restaurant.Name}</h2>
                <p className="bg-green-600 h-6 w-8 text-center rounded-lg text-white">{restaurant.Rating}</p>
              </div>
              <span className="text-xl font-semibold text-slate-600 mb-2">{restaurant.Location}</span>
              <div className="flex flex-row justify-between">
                <p className="text-red-500 text-lg">{restaurant.discount}</p>
                <button className="btn bg-slate-600 text-white p-1">Know more</button>
              </div>
            </div>
          </div>
        ))}
      </div>
    </div>
  );
};

export default ListCards;


